"""3. Создать текстовый файл (не программно). Построчно записать фамилии
сотрудников и величину их окладов (не менее 10 строк). Определить, кто из
сотрудников имеет оклад менее 20 тысяч, вывести фамилии этих сотрудников.
Выполнить подсчёт средней величины дохода сотрудников."""

with open('task_3.txt', 'r', encoding='utf-8') as file_3:
    employees = {}

    for line in file_3:
        key, value = line.split()
        employees[key] = value
        if int(value) < 20000:
            print(f'{key}: оклад менее 20 тысяч')
